package main

import "fmt"

func main() {
	var y int = 10               // Integer
	var z float32 = 6.78         // Float32
	var i string = "Beliz Cimen" // String
	var x bool = false           // Boolean

	fmt.Println("Boolean: ", x)
	fmt.Println("Integer: ", y)
	fmt.Println("Float:   ", z)
	fmt.Println("String:  ", i)
}
